CREATE 
    ALGORITHM = UNDEFINED 
    DEFINER = `root`@`localhost` 
    SQL SECURITY DEFINER
VIEW `Empresa` AS
    SELECT 
        `ENTIDADE`.`ENT_NOME` AS `ENT_NOME`,
        `ENTIDADE_ENDERECO`.`END_ENTIDADE_DESCRICAO` AS `END_ENTIDADE_DESCRICAO`,
        `ENTIDADE_ENDERECO`.`END_ENTIDADE_COMPLEMENTO` AS `END_ENTIDADE_COMPLEMENTO`,
        `ENTIDADE_TELEFONE`.`TEL_ENTIDADE_NUMERO` AS `TEL_ENTIDADE_NUMERO`
    FROM
        ((`ENTIDADE`
        JOIN `ENTIDADE_ENDERECO`)
        JOIN `ENTIDADE_TELEFONE`)
    WHERE
        ((`ENTIDADE`.`ENT_ID` = `ENTIDADE_ENDERECO`.`END_ENTIDADE_ID`)
            AND (`ENTIDADE`.`ENT_ID` = `ENTIDADE_TELEFONE`.`TEL_ENTIDADE_ID`)
            AND (`ENTIDADE`.`ENT_EMPRESA` = 'S'))
