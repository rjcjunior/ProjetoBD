CREATE DEFINER=`root`@`localhost` TRIGGER `EVENTO`.`PACOTE_ITEM_BEFORE_INSERT` BEFORE INSERT ON `PACOTE_ITEM` FOR EACH ROW
 BEGIN
 
DECLARE VALOR_PRODUTO float;

SELECT PRODUTO.P_VALOR FROM PRODUTO
 WHERE PRODUTO.P_ID = NEW.IPAC_PRODUTO 
INTO VALOR_PRODUTO;



SET NEW.IPAC_VALOR_UNITARIO = VALOR_PRODUTO,
 NEW.IPAC_VALOR_TOTAL = VALOR_PRODUTO*NEW.IPAC_QUANTIDADE; 


END
