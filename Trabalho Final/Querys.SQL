1 -  SELECT ENTIDADE.ENT_NOME, AVALIACAO_ITEM_PEDIDO.AVI_NOTA,AVALIACAO_ITEM_PEDIDO.AVI_PEDIDO,
 AVALIACAO_ITEM_PEDIDO.AVI_PEDIDO_ITEM
    FROM AVALIACAO_ITEM_PEDIDO,
    PEDIDO_ITEM,PRODUTO, ENTIDADE
    WHERE AVALIACAO_ITEM_PEDIDO.AVI_PEDIDO = PEDIDO_ITEM.PI_PEDIDO
    AND PEDIDO_ITEM.PI_PRODUTO = PRODUTO.P_ID 
    AND PRODUTO.P_ENTIDADE_ID = ENTIDADE.ENT_ID

    GROUP BY ENTIDADE.ENT_NOME, AVALIACAO_ITEM_PEDIDO.AVI_NOTA,AVALIACAO_ITEM_PEDIDO.AVI_PEDIDO,
 AVALIACAO_ITEM_PEDIDO.AVI_PEDIDO_ITEM
 ORDER BY AVI_PEDIDO

;



2 - SELECT ENTIDADE.ENT_NOME, ENTIDADE_ENDERECO.END_ENTIDADE_DESCRICAO,ENTIDADE_ENDERECO.END_ENTIDADE_COMPLEMENTO
FROM ENTIDADE, ENTIDADE_ENDERECO,PRODUTO 
WHERE ENTIDADE.ENT_ID = ENTIDADE_ENDERECO.END_ENTIDADE_ID 
AND PRODUTO.P_ENTIDADE_ID = ENTIDADE.ENT_ID
 AND PRODUTO.P_NOME = 'SALAO'



3 - SELECT ENTIDADE_TELEFONE.TEL_ENTIDADE_NUMERO 
FROM ENTIDADE_TELEFONE,ENTIDADE,PRODUTO
WHERE ENTIDADE_TELEFONE.TEL_ENTIDADE_ID = ENTIDADE.ENT_ID
 AND ENTIDADE.ENT_ID = PRODUTO.P_ENTIDADE_ID
 AND PRODUTO.P_NOME = 'GELO'




;




